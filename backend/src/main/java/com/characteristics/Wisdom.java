package com.characteristics;

public class Wisdom {
	
	private int enlightenment;
	private int judgment;
	private int wile;
	private int willPower;
	private int intuitiveness;
	
	public Wisdom() {}

	public int getEnlightenment() {
		return enlightenment;
	}

	public void setEnlightenment(int enlightenment) {
		this.enlightenment = enlightenment;
	}

	public int getJudgment() {
		return judgment;
	}

	public void setJudgment(int judgment) {
		this.judgment = judgment;
	}

	public int getWile() {
		return wile;
	}

	public void setWile(int wile) {
		this.wile = wile;
	}

	public int getWillPower() {
		return willPower;
	}

	public void setWillPower(int willPower) {
		this.willPower = willPower;
	}

	public int getIntuitiveness() {
		return intuitiveness;
	}

	public void setIntuitiveness(int intuitiveness) {
		this.intuitiveness = intuitiveness;
	};
	
	

}
